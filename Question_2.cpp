//Area of a circle
#include <iostream>

using namespace std;

int main()
{
    float pi=3.1416;
    float radius=0.0;
    cout<<"Enter the value of the radius:\n";
    cin>>radius;
    float area=pi*radius*radius;
    cout<<"The area of the circle with radius, "<<radius<<" is = "<<area<<" square units";
    
}
